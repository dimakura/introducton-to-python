# ANCHOR: numbers
age = 33
print(age == 33)
print(age != 18)
# ANCHOR_END: numbers

# ANCHOR: less-than
print(age < 40) # less than
# ANCHOR_END: less-than

# ANCHOR: greater-than
print(age > 40) # greater than
# ANCHOR_END: greater-than

# ANCHOR: less-than-equal
print(age <= 33) # less than or equal to
# ANCHOR_END: less-than-equal

# ANCHOR: greater-than-equal
print(age >= 33) # greater than or equal to
# ANCHOR_END: greater-than-equal